import sys
from pathlib import Path

import pytest

from .conftest import PROJECT_ROOT


@pytest.mark.genai
class TestAcceptanceValidatorSessionScoping:
    """Acceptance criteria from Issue #587."""

    # -------------------------------------------------------------------------
