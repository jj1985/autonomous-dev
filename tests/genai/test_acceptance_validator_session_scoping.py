    def test_single_session_ordering_violation_still_detected(self):
        """A real ordering violation within a single session must still be detected."""
        sys.path.insert(0, str(PROJECT_ROOT / "plugins/autonomous-dev/lib"))
        from pipeline_intent_validator import validate_pipeline_intent
        import tempfile

        # Single session with implementer before planner (violation)
