from collections import Counter

import pytest

from .conftest import PROJECT_ROOT

DATASET_PATH = PROJECT_ROOT / "tests/benchmarks/reviewer/dataset.json"
TAXONOMY_PATH = PROJECT_ROOT / "tests/benchmarks/reviewer/taxonomy.json"
