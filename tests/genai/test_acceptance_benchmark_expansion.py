MINE_GIT_SCRIPT = PROJECT_ROOT / "scripts/mine_git_samples.py"
MINE_SESSION_SCRIPT = PROJECT_ROOT / "scripts/mine_session_logs.py"


def _load_samples(data: dict) -> list:
    """Return sample list from dataset regardless of top-level format."""
    if isinstance(data, list):
        return data
    if "samples" in data:
        return data["samples"]
    return []


