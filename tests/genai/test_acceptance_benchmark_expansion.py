@pytest.mark.genai
class TestCleanRatio:
    """At least 20-25% of samples must have expected_verdict APPROVE."""

    def test_approve_ratio_meets_minimum(self):
        """APPROVE samples must be at least 20% of total dataset."""
        data = json.loads(DATASET_PATH.read_text())
        samples = _load_samples(data)
        total = len(samples)
        assert total > 0, "Dataset is empty"
        approve_count = sum(1 for s in samples if s.get("expected_verdict") == "APPROVE")
        ratio = approve_count / total
        assert ratio >= 0.20, (
            f"APPROVE ratio is {ratio:.1%} ({approve_count}/{total}); "
            "minimum required is 20% to avoid reviewer recall bias."
        )

    def test_approve_ratio_does_not_exceed_ceiling(self):
        """APPROVE samples should not exceed 50% (dataset would be unrealistic)."""
        data = json.loads(DATASET_PATH.read_text())
        samples = _load_samples(data)
        total = len(samples)
        if total == 0:
            pytest.skip("Dataset is empty")
        approve_count = sum(1 for s in samples if s.get("expected_verdict") == "APPROVE")
        ratio = approve_count / total
        assert ratio <= 0.50, (
            f"APPROVE ratio is {ratio:.1%} — dataset skews too clean; "
            "real codebases have more defects than approvals."
        )


