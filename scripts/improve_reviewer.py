#!/usr/bin/env python3
"""Automated reviewer improvement loop: benchmark -> analyze -> improve -> verify.

Runs a baseline benchmark, identifies weaknesses, generates improvement
instructions, applies them to reviewer.md, runs a verification benchmark,
and checks for regressions.

Usage:
    python scripts/improve_reviewer.py \
        --dataset tests/benchmarks/reviewer/dataset.json \
        --model claude-sonnet-4-20250514 \
        --trials 3 \
        --dry-run

GitHub Issue: #578
"""

